const _ = require('lodash');
const shlex = require('shlex');

const get_slug = text => {
	let cleaned = text.toLowerCase();
	cleaned = _.deburr(cleaned);
	cleaned = cleaned.replace(/[\W\s\:\/\\]+/g, '-');
	cleaned = cleaned.replace(/^-|-$/g, '');
	return cleaned;
};

const quote_if_necessary = shlex.quote;
const unquote = text => {
	const first_letter = _.first(text);
	const last_letter = _.last(text);

	if(first_letter == last_letter) {
		if(['"', "'"].indexOf(first_letter) > -1) {
			return text.slice(1,-1);
		}
	}
	return text;
}
